x = min(5, 10, 25) # retorna menor números entre números informados
y = max(5, 10, 25) # retorna maior números entre números informados

print(x) # 5
print(y) # 25

print("\n--------------------------------")

x = abs(-7.25) # retorna o absoluto do número informado sempre positivo

print(x) # 7.25 

